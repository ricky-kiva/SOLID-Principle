// Abstraction: mechanism when process in an object is hidden

// difference between Abstraction & Encapsulation:
// - Abstraction focus on what does an object do, without knowing how they do it
// --- Encapsulation is more about how to hide a mechanism when an object is doing something
// - Encapsulation hides data using getter & setter to access it
// --- Abstraction hides implementation with `abstract class`, `interface`, etc

// `Abstraction Layer` / `Abstaction Level` is a mechanism to simplify the interface of a complex underlying system